"""
Some Counting
"""
#Loop 1
print("Loop 1: Count up from 0 to 50")
for i in range(0,50,1):
    print(i)
    
#Loop 2
print("\nLoop 2: Count down from 50 to 0")
for i in range(50,-1,11):
    print(i)
    
#Loop 3
print("\nLoop 3: Count up from 30 to 50")
for i in range(30,51,1):
    print(i)
    
#Loop 4
print("\nLoop 4: Count down from 50 to 10")
for i in range(50,9,-1):
    print(i)
    
#Loop 5
print("\nLoop 5: Count up from 100 to 200 in steps of 5")
for i in range(100,201,5):
    print(i)

    

